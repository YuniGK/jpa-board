    @DisplayName("[view][GET] 게시글 리스트 (게시판) 페이지 - 정상 호출")
    @Test
    void givenNothing_whenRequestingArticlesView_thenReturnsArticlesView() throws Exception {
        // Given
        given(articleService.searchArticles(eq(null), eq(null), any(Pageable.class))).willReturn(Page.empty());
        given(paginationService.getPaginationBarNumbers(anyInt(), anyInt())).willReturn(List.of(0, 1, 2, 3, 4));

        // When & Then
        /* mockMvc 메소드
        * perform() - 요청을 전송하는 역할
        *           결과로 ResultActions 객체를 받으며, ResultActions 객체는 리턴 값을 검증하고 확인할 수 있는 andExcpect() 메소드를 제공
        *
        * get(), post(), put(), delete() - HTTP 메소드를 결정, 인자로는 경로를 보내줌
        *
        * andExpect() - 응답을 검증하는 역할
        *
        * status() - 상태 코드
        *           isOk() : 200
        *           isNotFound() : 404
        *           isMethodNotAllowed() : 405
        *           isInternalServerError() : 500
        *           is(int status) : status 상태 코드
        *
        * view() - 뷰 / 리턴하는 뷰 이름을 검증
        *
        * redirect() - 리다이렉트 응답을 검증
        *
        * model() - 컨트롤러에서 저장한 모델들의 정보 검증
        *
        * content() - 응답에 대한 정보를 검증
        *
        * andDo(print()) - 요청/응답 전체 메세지를 확인 */
        mvc.perform(get("/articles"))
                .andExpect(status().isOk())
                .andExpect(content().contentTypeCompatibleWith(MediaType.TEXT_HTML))
                .andExpect(view().name("articles/index"))
                .andExpect(model().attributeExists("articles"))
                .andExpect(model().attributeExists("paginationBarNumbers"))
                .andExpect(model().attributeExists("searchTypes"))
                .andExpect(model().attribute("searchTypeHashtag", SearchType.HASHTAG));
        then(articleService).should().searchArticles(eq(null), eq(null), any(Pageable.class));
        then(paginationService).should().getPaginationBarNumbers(anyInt(), anyInt());
    }

